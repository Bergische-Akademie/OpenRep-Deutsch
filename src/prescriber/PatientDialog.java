private void OKButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKButtonActionPerformed
   if (NameEdit.getText().trim().equals("") || SurnameEdit.getText().trim().equals("")) {
       JOptionPane.showMessageDialog(rootPane, "Sie müssen mind. einen Namen und Vornamen für den Patienten angeben");
       return;
   }

   String temp = BirthEdit.getText().trim();
   String dob = "";
   if (!temp.equals("")) {
       String[] temps = temp.split("/");

       Calendar cal = Calendar.getInstance();

       int year, month, day;
       try {
           year = Integer.parseInt(temps[0]);
       } catch (Exception e) {
           year = -1;
       }

       try {
           month = Integer.parseInt(temps[1]);
       } catch (Exception e) {
           month = -1;
       }

       try {
           day = Integer.parseInt(temps[2]);
       } catch (Exception e) {
           day = -1;
       }       

       if (temps.length == 1) {
           if (year >= 150) dob = temps[0]+"/01/01";
           else
           dob = String.valueOf(cal.get(Calendar.YEAR) - year) +"/01/01";
       }
       else if (temps.length == 2) {
           if (year >= 150) {
               dob = temps[0] + "/" + month + "/01";
           }
           else {
               int mnth = (cal.get(Calendar.MONTH) + 1 - month);
               if (mnth <= 0) {
                   mnth = 12 + mnth;
                   year = cal.get(Calendar.YEAR) - year - 1;
               }
               else
               year = cal.get(Calendar.YEAR) - year;
               dob = String.valueOf(year) + "/" + (mnth) + "/01";
           }
           
       }
       else if (temps.length == 3) {
           dob = temps[0] + "/" + temps[1] + "/" + temps[2];
       }
       if ((temps.length == 3 && Utils.ConvertDateToAge(BirthEdit.getText()).equals("")) || temps.length > 3) {
          JOptionPane.showMessageDialog(rootPane, "Bitte geben Sie ein korrektes Datum an (jjj/mm/tt).");
          return;
       }
   }
   result = new Patient ();
   result.additional_information = AdditionalInformationEdit.getText();
   result.address = AddressEdit.getText().trim();
   result.comment = "";
   result.email = EmailEdit.getText().trim();
   result.name = NameEdit.getText().trim();
   result.surname = SurnameEdit.getText().trim();
   result.telephone = TelephoneEdit.getText().trim();
   result.sex = MaleRadio.isSelected();
   result.date_of_birth = dob;
   this.setVisible(false);
}//GEN-LAST:event_OKButtonActionPerformed

