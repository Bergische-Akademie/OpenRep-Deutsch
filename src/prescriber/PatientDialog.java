private void AdditionalInformationEditKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_AdditionalInformationEditKeyPressed
    if (evt.isAltDown()) {
        if (evt.getKeyCode() == evt.VK_UP) {
            Utils.ChangeFont(AdditionalInformationEdit, 1);
            main_editor.config.SetValue(Configuration.Key_PatientDialog_AdditionalInformationEdit, AdditionalInformationEdit.getFont().getSize());
        }
        else
        if (evt.getKeyCode() == evt.VK_DOWN) {
            Utils.ChangeFont(AdditionalInformationEdit, -1);
            main_editor.config.SetValue(Configuration.Key_PatientDialog_AdditionalInformationEdit, AdditionalInformationEdit.getFont().getSize());
        }
    }
}//GEN-LAST:event_AdditionalInformationEditKeyPressed

/** Sets the edits according to the parameter
 * 
 * @param p
 */
  public void SetData (Patient p) {
      if (p == null) return;
      AdditionalInformationEdit.setText(p.additional_information);
      AddressEdit.setText(p.address);
      EmailEdit.setText(p.email);
      NameEdit.setText(p.name);
      SurnameEdit.setText(p.surname);
      TelephoneEdit.setText(p.telephone);
      BirthEdit.setText(p.date_of_birth);
      MaleRadio.setSelected(p.sex);
  }

