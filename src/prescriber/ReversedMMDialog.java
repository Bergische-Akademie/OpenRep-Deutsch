    /** Converts the treepath of the SymptomList to the correct name of the symptom
     * 
     * @param tp
     * @return
     */
    private String ConvertTreePathToRemedyName (TreePath tp) {    
       String temps = "";
       if (tp == null) return "";
       for (int x = 1; x < tp.getPathCount(); x++)                
            temps += tp.getPath()[x].toString();
       return temps;
    }

